const mongoose = require('mongoose');

const connectionUserSchema = new mongoose.Schema({
    reporterId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    reportedUserId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

const ConnectionUser = mongoose.model('ConnectionUser', connectionUserSchema);

module.exports = ConnectionUser;
